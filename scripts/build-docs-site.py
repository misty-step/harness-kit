def icon_svg(name: str, current: str) -> str:
    if name not in ICON_PATHS:
        raise SystemExit(f"unknown icon: {name}")
    href = f"{rel(current, 'assets/icons.svg')}#icon-{name}"
    return f'<svg class="icon" aria-hidden="true"><use href="{href}"></use></svg>'


def render_icons() -> str:
    symbols = []
    for name, path in sorted(ICON_PATHS.items()):
        symbols.append(f'<symbol id="icon-{name}" viewBox="0 0 24 24">{path}</symbol>')
    return (
        f"<!-- {GENERATED} -->\n"
        '<svg xmlns="http://www.w3.org/2000/svg" style="display:none" fill="none" '
        'stroke="currentColor" stroke-width="1.8" stroke-linecap="round" stroke-linejoin="round">\n'
        + "\n".join(symbols)
        + "\n</svg>\n"
    )


def slug(value: str) -> str:
    text = re.sub(r"[^a-z0-9-]+", "-", value.lower()).strip("-")
    return text or "item"


def read(path: Path) -> str:
    return path.read_text(encoding="utf-8")


def parse_frontmatter(path: Path) -> dict[str, str]:
    text = read(path)
    if not text.startswith("---\n"):
        return {}
    parts = text.split("---", 2)
    if len(parts) < 3:
        return {}
    lines = parts[1].splitlines()
    data: dict[str, str] = {}
    index = 0
    while index < len(lines):
        line = lines[index]
        if not line or line.startswith(" "):
            index += 1
            continue
        if ":" not in line:
            index += 1
            continue
        key, raw_value = line.split(":", 1)
        value = raw_value.strip()
        if value == "|":
            collected: list[str] = []
            index += 1
            while index < len(lines) and lines[index].startswith("  "):
                collected.append(lines[index].strip())
                index += 1
            data[key.strip()] = " ".join(collected).strip()
            continue
        data[key.strip()] = value.strip("\"'")
        index += 1
    return data


def first_heading(path: Path) -> str:
    for line in read(path).splitlines():
        if line.startswith("# "):
            return line[2:].strip()
    return path.stem


def primitive_description(path: Path) -> str:
    frontmatter = parse_frontmatter(path)
    description = frontmatter.get("description", "").strip()
    if description:
        return re.sub(r"\s+", " ", description)
    return first_heading(path)


def collect_primitives() -> list[Primitive]:
    primitives: list[Primitive] = []
    for skill_md in sorted((ROOT / "skills").glob("*/SKILL.md")):
        name = skill_md.parent.name
        source = skill_md.relative_to(ROOT).as_posix()
        primitives.append(
            Primitive(
                kind="skill",
                name=name,
                description=primitive_description(skill_md),
                source=source,
                page=f"reference/skills/{slug(name)}.html",
                role=WORKFLOW_ROLES.get(name, "Support"),
            )
        )
    for agent_md in sorted((ROOT / "agents").glob("*.md")):
        name = agent_md.stem
        source = agent_md.relative_to(ROOT).as_posix()
        primitives.append(
            Primitive(
                kind="agent",
                name=name,
                description=primitive_description(agent_md),
                source=source,
                page=f"reference/agents/{slug(name)}.html",
                role="Specialist",
            )
        )
    return primitives


def collect_gates() -> list[str]:
    text = read(ROOT / "ci/src/spellbook_ci/main.py")
    return sorted(set(re.findall(r'run_gate,\s+"([^"]+)"', text)))


def collect_backlog() -> list[dict[str, str]]:
    items: list[dict[str, str]] = []
    for path in sorted((ROOT / "backlog.d").glob("[0-9]*.md")):
        text = read(path)
        title = text.splitlines()[0].lstrip("# ").strip() if text else path.stem
        items.append({"title": title, "source": path.relative_to(ROOT).as_posix()})
    return items


def rel(current: str, target: str) -> str:
    current_parent = Path(current).parent
    if current_parent == Path("."):
        return Path(target).relative_to(".").as_posix()
    return "../" * len(current_parent.parts) + target


def esc(value: object) -> str:
    return html.escape(str(value), quote=True)


def script_json(value: str) -> str:
    return value.replace("<", "\\u003c").replace("&", "\\u0026")


def trigger_for(primitive: Primitive) -> str:
    return f"/{primitive.name}" if primitive.kind == "skill" else primitive.name


def page_excerpt(source: str) -> str:
    path = ROOT / source
    lines = []
    in_frontmatter = False
    started = False
    for raw in read(path).splitlines():
        line = raw.rstrip()
        if line == "---":
            in_frontmatter = not in_frontmatter
            continue
        if in_frontmatter:
            continue
        if line.startswith("# ") and not started:
            started = True
            continue
        if not line.strip() and not lines:
            continue
        lines.append(line)
        if len("\n".join(lines)) > 1800:
            break
    excerpt = "\n".join(lines).strip()
    return excerpt + ("\n..." if len(excerpt) > 1750 else "")


def render_excerpt(source: str) -> str:
    excerpt = page_excerpt(source)
    if not excerpt:
        return ""
    return f'<pre class="source-excerpt"><code>{esc(excerpt)}</code></pre>'


