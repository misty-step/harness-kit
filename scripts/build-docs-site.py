def write_file(output: Path, relative: str, content: str) -> None:
    path = output / relative
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content, encoding="utf-8")


def build(output: Path) -> None:
    if output.exists():
        shutil.rmtree(output)
    output.mkdir(parents=True)
    primitives = collect_primitives()
    gates = collect_gates()
    backlog = collect_backlog()
    write_file(output, "index.html", render_home(primitives, backlog))
    write_file(output, "concepts.html", render_concepts())
    write_file(output, "workflows.html", render_workflows())
    for workflow in workflows():
        write_file(output, workflow_page(workflow), render_workflow_page(workflow))
    write_file(output, "governance.html", render_governance())
    write_file(output, "reference/index.html", render_reference(primitives, gates))
    write_file(output, "reference/gates.html", render_gates(gates))
    write_file(output, "reference/bootstrap.html", render_bootstrap())
    for primitive in primitives:
        write_file(output, primitive.page, render_primitive(primitive))
    write_file(output, "manifest.json", render_manifest(primitives, gates, backlog))
    write_file(output, "llms.txt", render_llms(primitives, gates))
    write_file(output, ".nojekyll", "")
    write_file(output, "assets/site.css", f"/* {GENERATED} */\n{CSS}")
    write_file(output, "assets/catalog.js", f"// {GENERATED}\n{JS}")
    write_file(output, "assets/icons.svg", render_icons())


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--output", type=Path, default=ROOT / "docs/site")
    args = parser.parse_args()
    output = args.output if args.output.is_absolute() else ROOT / args.output
    build(output)
    print(f"Built {output.relative_to(ROOT) if output.is_relative_to(ROOT) else output}")
    return 0


