def load_copy() -> dict[str, Any]:
    try:
        data = json.loads(COPY_SOURCE.read_text(encoding="utf-8"))
    except FileNotFoundError as exc:
        raise SystemExit(f"{COPY_SOURCE.relative_to(ROOT)} is missing") from exc
    require(data, "home", dict)
    require(data, "concepts", dict)
    require(data, "governance", dict)
    require(data, "workflows", list)
    require(data, "role_icons", dict)
    validate_icon_names(data)
    return data


def require(data: dict[str, Any], key: str, expected_type: type) -> Any:
    value = data.get(key)
    if not isinstance(value, expected_type):
        raise SystemExit(f"{COPY_SOURCE.relative_to(ROOT)}: {key} must be {expected_type.__name__}")
    return value


def validate_icon_names(data: dict[str, Any]) -> None:
    used: set[str] = set()
    for card_data in data.get("home", {}).get("start_cards", []):
        used.add(str(card_data.get("icon", "")))
    for concept in data.get("concepts", {}).get("items", []):
        if len(concept) >= 3:
            used.add(str(concept[2]))
    for workflow in data.get("workflows", []):
        used.add(str(workflow.get("icon", "")))
    for card_data in data.get("governance", {}).get("cards", []):
        if len(card_data) >= 3:
            used.add(str(card_data[2]))
    used.update(str(value) for value in data.get("role_icons", {}).values())
    missing = sorted(name for name in used if name and name not in ICON_PATHS)
    if missing:
        raise SystemExit(f"{COPY_SOURCE.relative_to(ROOT)} references unknown icon(s): {', '.join(missing)}")


