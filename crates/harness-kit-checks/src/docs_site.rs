use std::collections::{BTreeMap, BTreeSet};
use std::fs;
use std::path::{Path, PathBuf};
use std::process::Command;

use anyhow::{Context, Result, bail};
use regex::Regex;
use serde_json::{Map, Value, json};
use sha2::{Digest, Sha256};

const SITE_IMAGE_ROOT: &str = "assets/images";
const GENERATED: &str = "Generated by harness-kit-checks build-docs-site; do not edit manually.";

#[derive(Debug, Clone)]
pub struct CheckOptions {
    pub repo_root: PathBuf,
    pub site: PathBuf,
    pub compare_to_rebuild: bool,
}

pub fn default_site(repo_root: &Path) -> PathBuf {
    repo_root.join("docs/site")
}

pub fn validate_site(options: &CheckOptions) -> Result<()> {
    let repo = &options.repo_root;
    let site = &options.site;
    if !site.exists() {
        bail!(
            "{}: missing; run harness-kit-checks build-docs-site",
            site.display()
        );
    }

    let copy = load_copy(repo)?;
    let expected = expected_primitives(repo)?;
    let mut seen: BTreeMap<String, Vec<PathBuf>> = BTreeMap::new();
    let mut source_seen: BTreeMap<String, Vec<PathBuf>> = BTreeMap::new();
    for page in glob_html(&site.join("reference"))? {
        if page.parent() == Some(&site.join("reference")) {
            continue;
        }
        let (primitive, source) = page_metadata(&page)?;
        if let Some(primitive) = primitive {
            seen.entry(primitive).or_default().push(page.clone());
        }
        if let Some(source) = source {
            source_seen
                .entry(source.clone())
                .or_default()
                .push(page.clone());
            if !repo.join(&source).exists() {
                bail!("{}: stale source link {source}", page.display());
            }
        }
    }

    let expected_keys: BTreeSet<_> = expected.keys().cloned().collect();
    let seen_keys: BTreeSet<_> = seen.keys().cloned().collect();
    let missing: Vec<_> = expected_keys.difference(&seen_keys).cloned().collect();
    if !missing.is_empty() {
        bail!("missing primitive page(s): {}", missing.join(", "));
    }
    let extra: Vec<_> = seen_keys.difference(&expected_keys).cloned().collect();
    if !extra.is_empty() {
        bail!("unexpected primitive page(s): {}", extra.join(", "));
    }
    let duplicates: Vec<_> = seen
        .iter()
        .filter_map(|(key, paths)| (paths.len() != 1).then_some(key.clone()))
        .collect();
    if !duplicates.is_empty() {
        bail!("duplicate primitive page(s): {}", duplicates.join(", "));
    }
    let duplicate_sources: Vec<_> = source_seen
        .iter()
        .filter_map(|(key, paths)| (paths.len() != 1).then_some(key.clone()))
        .collect();
    if !duplicate_sources.is_empty() {
        bail!("duplicate source link(s): {}", duplicate_sources.join(", "));
    }

    let manifest_path = site.join("manifest.json");
    let manifest: Value = serde_json::from_str(
        &fs::read_to_string(&manifest_path)
            .with_context(|| format!("failed to read {}", manifest_path.display()))?,
    )
    .with_context(|| format!("{}: invalid JSON", manifest_path.display()))?;
    if manifest.get("copy_source").and_then(Value::as_str) != Some("docs/copy/site.json") {
        bail!(
            "{}: copy_source must be docs/copy/site.json",
            manifest_path.display()
        );
    }
    let manifest_ids: Vec<_> = array(&manifest, "primitives")
        .iter()
        .filter_map(|item| item.get("id").and_then(Value::as_str).map(str::to_string))
        .collect();
    if manifest_ids.iter().cloned().collect::<BTreeSet<_>>() != expected_keys {
        bail!(
            "{}: primitive manifest does not match source catalog",
            manifest_path.display()
        );
    }
    for item in array(&manifest, "primitives") {
        let id = item.get("id").and_then(Value::as_str).unwrap_or("unknown");
        let page = string_field(item, "page", &manifest_path)?;
        let source = string_field(item, "source", &manifest_path)?;
        if !site.join(&page).exists() {
            bail!("{}: page missing for {id}: {page}", manifest_path.display());
        }
        if !repo.join(&source).exists() {
            bail!(
                "{}: source missing for {id}: {source}",
                manifest_path.display()
            );
        }
    }
    validate_images(repo, &copy, &manifest, site)?;

    for relative in [
        "index.html",
        "concepts.html",
        "workflows.html",
        "governance.html",
        "reference/index.html",
        "reference/gates.html",
        "reference/bootstrap.html",
        "llms.txt",
        ".nojekyll",
    ] {
        let path = site.join(relative);
        if !path.exists() {
            bail!("{}: missing required page", path.display());
        }
        let text = fs::read_to_string(&path)
            .with_context(|| format!("failed to read {}", path.display()))?;
        if relative.ends_with(".html") && !text.contains(GENERATED) {
            bail!("{}: missing generated marker", path.display());
        }
    }

    let workflows = array(&copy, "workflows");
    if workflows.len() < 5 {
        bail!(
            "{}: expected at least five workflows",
            copy_source(repo).display()
        );
    }
    let expected_workflow_pages: BTreeSet<_> = workflows
        .iter()
        .filter_map(|item| item.as_object())
        .map(|item| {
            format!(
                "{}.html",
                slug(item.get("title").and_then(Value::as_str).unwrap_or(""))
            )
        })
        .collect();
    let workflow_pages = direct_html_files(&site.join("workflows"))?;
    let workflow_page_names: BTreeSet<_> = workflow_pages
        .iter()
        .filter_map(|path| {
            path.file_name()
                .and_then(|name| name.to_str())
                .map(str::to_string)
        })
        .collect();
    if workflow_page_names != expected_workflow_pages {
        bail!(
            "{}: workflow pages do not match docs/copy/site.json",
            site.join("workflows").display()
        );
    }
    for page in workflow_pages {
        let text = fs::read_to_string(&page)
            .with_context(|| format!("failed to read {}", page.display()))?;
        for phrase in [
            "Input",
            "Decision",
            "Evidence",
            "Failure handling",
            "What to verify",
        ] {
            if !text.contains(phrase) {
                bail!("{}: missing workflow section {phrase}", page.display());
            }
        }
    }

    let icons_path = site.join("assets/icons.svg");
    if !icons_path.exists() {
        bail!("{}: missing generated icon sprite", icons_path.display());
    }
    let icons_text = fs::read_to_string(&icons_path)
        .with_context(|| format!("failed to read {}", icons_path.display()))?;
    let manifest_icons: BTreeSet<_> = array(&manifest, "icons")
        .iter()
        .filter_map(|value| value.as_str().map(str::to_string))
        .collect();
    for icon in required_icons(&copy, &manifest) {
        if !manifest_icons.contains(&icon) {
            bail!(
                "{}: icon {icon} is used but missing from manifest icons",
                manifest_path.display()
            );
        }
        if !icons_text.contains(&format!("id=\"icon-{icon}\"")) {
            bail!("{}: missing symbol for icon {icon}", icons_path.display());
        }
    }

    for path in all_files(site)? {
        let text = fs::read_to_string(&path)
            .with_context(|| format!("failed to read {}", path.display()))?;
        for pattern in private_patterns()? {
            if pattern.is_match(&text) {
                bail!(
                    "{}: generated docs contain private or local-only text matching {}",
                    path.display(),
                    pattern.as_str()
                );
            }
        }
    }

    if options.compare_to_rebuild {
        let temp = tempfile::tempdir().context("failed to create temporary directory")?;
        let rebuilt = temp.path().join("site");
        let output = Command::new("cargo")
            .args([
                "run",
                "--locked",
                "-p",
                "harness-kit-checks",
                "--",
                "build-docs-site",
                "--output",
            ])
            .arg(&rebuilt)
            .current_dir(repo)
            .output()
            .context("failed to run harness-kit-checks build-docs-site")?;
        if !output.status.success() {
            bail!(
                "harness-kit-checks build-docs-site failed: {}{}",
                String::from_utf8_lossy(&output.stdout),
                String::from_utf8_lossy(&output.stderr)
            );
        }
        let issues = compare_dirs(&rebuilt, site)?;
        if !issues.is_empty() {
            bail!(
                "docs/site is stale:\n{}",
                issues.into_iter().take(40).collect::<Vec<_>>().join("\n")
            );
        }
    }
    Ok(())
}

pub fn self_test(repo: &Path) -> Result<()> {
    validate_site(&CheckOptions {
        repo_root: repo.to_path_buf(),
        site: default_site(repo),
        compare_to_rebuild: true,
    })?;
    let copy = load_copy(repo)?;
    let images = image_declarations(repo, &copy)?;
    let skill_pages = direct_html_files(&repo.join("docs/site/reference/skills"))?;
    if skill_pages.is_empty() {
        bail!("self-test needs at least one generated skill page");
    }

    mutate_site_expect_failure(repo, "missing generated skill page", |base| {
        let page = direct_html_files(&base.join("reference/skills"))?
            .into_iter()
            .next()
            .context("missing copied skill page")?;
        fs::remove_file(page)?;
        Ok(())
    })?;
    mutate_site_expect_failure(repo, "duplicate generated primitive page", |base| {
        let page = direct_html_files(&base.join("reference/skills"))?
            .into_iter()
            .next()
            .context("missing copied skill page")?;
        fs::copy(&page, page.with_file_name("duplicate.html"))?;
        Ok(())
    })?;
    mutate_site_expect_failure(repo, "stale source link", |base| {
        let page = direct_html_files(&base.join("reference/skills"))?
            .into_iter()
            .next()
            .context("missing copied skill page")?;
        let text = fs::read_to_string(&page)?;
        let replaced = Regex::new(r#"<meta name="harness-kit-source" content="[^"]+">"#)?
            .replace(
                &text,
                r#"<meta name="harness-kit-source" content="missing/source.md">"#,
            )
            .to_string();
        fs::write(page, replaced)?;
        Ok(())
    })?;

    if !images.is_empty() {
        mutate_site_expect_failure(repo, "missing generated image", |base| {
            let image = all_files(&base.join(SITE_IMAGE_ROOT))?
                .into_iter()
                .next()
                .context("missing copied image")?;
            fs::remove_file(image)?;
            Ok(())
        })?;
        mutate_site_expect_failure(repo, "generated image byte drift", |base| {
            let image = all_files(&base.join(SITE_IMAGE_ROOT))?
                .into_iter()
                .next()
                .context("missing copied image")?;
            let mut text = fs::read_to_string(&image)?;
            text.push_str("\n<!-- drift -->\n");
            fs::write(image, text)?;
            Ok(())
        })?;
        mutate_site_expect_failure(repo, "empty or mismatched image alt", |base| {
            let page = base.join(string_field(&images[0], "page", &copy_source(repo))?);
            let text = fs::read_to_string(&page)?;
            let replaced = Regex::new(r#"alt="[^"]+""#)?
                .replace(&text, r#"alt="""#)
                .to_string();
            fs::write(page, replaced)?;
            Ok(())
        })?;
        mutate_site_expect_failure(repo, "missing text-equivalent anchor", |base| {
            let page = base.join(string_field(&images[0], "page", &copy_source(repo))?);
            let anchor = string_field(&images[0], "text_equivalent_anchor", &copy_source(repo))?;
            let text = fs::read_to_string(&page)?
                .replace(&format!("id=\"{anchor}\""), "id=\"missing-anchor\"");
            fs::write(page, text)?;
            Ok(())
        })?;
        mutate_site_expect_failure(repo, "undeclared html image", |base| {
            let page = base.join(string_field(&images[0], "page", &copy_source(repo))?);
            let text = fs::read_to_string(&page)?.replacen(
                "</section>",
                r#"<img src="../assets/images/undeclared.svg" alt="Undeclared">
</section>"#,
                1,
            );
            fs::write(page, text)?;
            Ok(())
        })?;
        mutate_site_expect_failure(repo, "stale generated image", |base| {
            fs::write(
                base.join(SITE_IMAGE_ROOT).join("stale.svg"),
                r#"<svg xmlns="http://www.w3.org/2000/svg"/>"#,
            )?;
            Ok(())
        })?;

        expect_copy_failure(repo, "missing source policy", |copy| {
            array_mut(copy, "images")?[0]
                .as_object_mut()
                .context("image must be object")?
                .remove("source_policy");
            Ok(())
        })?;
        expect_copy_failure(repo, "missing review note", |copy| {
            array_mut(copy, "images")?[0]["review_note"] = json!("");
            Ok(())
        })?;
        expect_copy_failure(repo, "image path traversal", |copy| {
            array_mut(copy, "images")?[0]["source"] = json!("../secrets.svg");
            Ok(())
        })?;
        expect_copy_failure(repo, "unused image declaration", |copy| {
            array_mut(copy, "images")?[0]["page"] = json!("workflows/missing.html");
            Ok(())
        })?;
        expect_copy_failure(repo, "image size overflow", |copy| {
            array_mut(copy, "images")?[0]["max_bytes"] = json!(1);
            Ok(())
        })?;

        let temp = tempfile::tempdir().context("failed to create temporary directory")?;
        let copied_repo = temp.path().join("repo");
        copy_repo_for_self_test(repo, &copied_repo)?;
        let copy_path = copied_repo.join("docs/copy/site.json");
        let mut data: Value = serde_json::from_str(&fs::read_to_string(&copy_path)?)?;
        data.as_object_mut()
            .context("docs copy root must be object")?
            .remove("images");
        fs::write(&copy_path, serde_json::to_string_pretty(&data)? + "\n")?;
        let image_root = copied_repo.join("docs/copy/images");
        if image_root.exists() {
            fs::remove_dir_all(image_root)?;
        }
        run_checked(
            Command::new("cargo")
                .args([
                    "run",
                    "--locked",
                    "-p",
                    "harness-kit-checks",
                    "--",
                    "build-docs-site",
                ])
                .current_dir(&copied_repo),
            "harness-kit-checks build-docs-site",
        )?;
        run_checked(
            Command::new("cargo")
                .args([
                    "run",
                    "--locked",
                    "-p",
                    "harness-kit-checks",
                    "--",
                    "check-docs-site",
                    "--repo",
                    ".",
                ])
                .current_dir(&copied_repo),
            "harness-kit-checks check-docs-site",
        )?;
    }
    Ok(())
}

pub fn build_site(repo: &Path, output: &Path) -> Result<()> {
    let copy = load_copy(repo)?;
    let primitives = render_primitives(repo)?;
    let gates = collect_gates(repo)?;
    let backlog = collect_backlog(repo)?;
    let images = image_declarations(repo, &copy)?;
    if output.exists() {
        fs::remove_dir_all(output)
            .with_context(|| format!("failed to remove {}", output.display()))?;
    }
    fs::create_dir_all(output)?;

    write_file(
        output,
        "index.html",
        &page(
            "Start",
            "index.html",
            &format!(
                r#"<section><p class="eyebrow">Harness Kit</p><h1>Harness Kit generated documentation</h1><p>This site is generated from the live repository.</p><p>Skills: {}. Agents: {}. Gates: {}.</p></section>"#,
                primitives
                    .iter()
                    .filter(|primitive| primitive.kind == "skill")
                    .count(),
                primitives
                    .iter()
                    .filter(|primitive| primitive.kind == "agent")
                    .count(),
                gates.len()
            ),
        ),
    )?;
    write_file(
        output,
        "concepts.html",
        &page(
            "Concepts",
            "concepts.html",
            "<section><h1>Concepts</h1><p>Harness Kit keeps skills, agents, gates, and operating doctrine source-backed.</p></section>",
        ),
    )?;
    write_file(
        output,
        "workflows.html",
        &page(
            "Workflows",
            "workflows.html",
            &render_workflows_index(&copy),
        ),
    )?;
    for workflow in array(&copy, "workflows") {
        if let Some(workflow) = workflow.as_object() {
            let title = workflow.get("title").and_then(Value::as_str).unwrap_or("");
            let path = format!("workflows/{}.html", slug(title));
            write_file(
                output,
                &path,
                &page(title, &path, &render_workflow_page(repo, &copy, workflow)?),
            )?;
        }
    }
    write_file(
        output,
        "governance.html",
        &page(
            "Governance",
            "governance.html",
            "<section><h1>Governance</h1><p>Provider output is evidence, not authority. Completion claims require live artifacts.</p></section>",
        ),
    )?;
    write_file(
        output,
        "reference/index.html",
        &page(
            "Reference",
            "reference/index.html",
            &render_reference_index(&primitives, &gates),
        ),
    )?;
    write_file(
        output,
        "reference/gates.html",
        &page(
            "CI Gates",
            "reference/gates.html",
            &render_gates_page(&gates),
        ),
    )?;
    write_file(
        output,
        "reference/bootstrap.html",
        &page(
            "Bootstrap",
            "reference/bootstrap.html",
            r#"<section><h1>Bootstrap behavior</h1><p>harness-kit-checks bootstrap installs system-wide harness links and provider roster files; bootstrap.sh is the curl-compatible launcher.</p></section>"#,
        ),
    )?;
    for primitive in &primitives {
        write_file(output, &primitive.page, &render_primitive_page(primitive))?;
    }
    for image in &images {
        let output_path = output.join(string_field(image, "output", &copy_source(repo))?);
        fs::create_dir_all(
            output_path
                .parent()
                .context("image output missing parent")?,
        )?;
        fs::copy(
            repo.join(string_field(image, "source", &copy_source(repo))?),
            output_path,
        )?;
    }
    write_file(
        output,
        "manifest.json",
        &render_manifest(repo, &copy, &primitives, &gates, &backlog, &images)?,
    )?;
    write_file(
        output,
        "llms.txt",
        &render_llms(&primitives, &gates, &images)?,
    )?;
    write_file(output, ".nojekyll", "")?;
    write_file(
        output,
        "assets/site.css",
        &format!(
            "/* {GENERATED} */\nbody{{font-family:system-ui,sans-serif;line-height:1.5;margin:0;color:#111827;background:#fbfcff}}main,header{{max-width:1100px;margin:auto;padding:24px}}nav a{{margin-right:12px}}section{{margin:24px 0}}.card{{border:1px solid #d9dee8;border-radius:8px;padding:16px;margin:12px 0;background:white}}code{{background:#eef5ff;padding:2px 5px;border-radius:4px}}img{{max-width:100%;height:auto}}.source{{color:#5b6575}}\n"
        ),
    )?;
    write_file(output, "assets/catalog.js", &format!("// {GENERATED}\n"))?;
    write_file(
        output,
        "assets/icons.svg",
        &render_icons(&copy, &primitives)?,
    )?;
    Ok(())
}

#[derive(Debug, Clone)]
struct RenderPrimitive {
    kind: String,
    name: String,
    description: String,
    source: String,
    page: String,
    role: String,
    icon: String,
}

impl RenderPrimitive {
    fn id(&self) -> String {
        format!("{}:{}", self.kind, self.name)
    }
}

fn render_primitives(repo: &Path) -> Result<Vec<RenderPrimitive>> {
    let role_icons = load_copy(repo)?
        .get("role_icons")
        .and_then(Value::as_object)
        .cloned()
        .unwrap_or_default();
    let mut primitives = Vec::new();
    for (id, source) in expected_primitives(repo)? {
        let (kind, name) = id
            .split_once(':')
            .context("primitive id must contain kind prefix")?;
        let role = if kind == "agent" {
            "Specialist".to_string()
        } else {
            workflow_role(name).to_string()
        };
        let icon = role_icons
            .get(&role)
            .and_then(Value::as_str)
            .unwrap_or("catalog")
            .to_string();
        primitives.push(RenderPrimitive {
            kind: kind.to_string(),
            name: name.to_string(),
            description: primitive_description(&repo.join(&source))?,
            source,
            page: format!("reference/{}s/{}.html", kind, slug(name)),
            role,
            icon,
        });
    }
    primitives.sort_by_key(|primitive| primitive.id());
    Ok(primitives)
}

fn primitive_description(path: &Path) -> Result<String> {
    let text = fs::read_to_string(path)?;
    if let Some(frontmatter) = text
        .strip_prefix("---\n")
        .and_then(|rest| rest.split_once("\n---"))
    {
        let lines: Vec<_> = frontmatter.0.lines().collect();
        let mut index = 0;
        while index < lines.len() {
            let line = lines[index];
            if let Some((key, raw)) = line.split_once(':')
                && key.trim() == "description"
            {
                let raw = raw.trim();
                if raw == "|" {
                    let mut parts = Vec::new();
                    index += 1;
                    while index < lines.len() && lines[index].starts_with("  ") {
                        parts.push(lines[index].trim());
                        index += 1;
                    }
                    return Ok(parts.join(" "));
                }
                return Ok(raw.trim_matches('"').trim_matches('\'').to_string());
            }
            index += 1;
        }
    }
    Ok(text
        .lines()
        .find_map(|line| line.strip_prefix("# ").map(str::trim))
        .unwrap_or_else(|| {
            path.file_stem()
                .and_then(|name| name.to_str())
                .unwrap_or("item")
        })
        .to_string())
}

fn workflow_role(name: &str) -> &'static str {
    match name {
        "shape" | "groom" => "Plan",
        "implement" => "Build",
        "deliver" => "Orchestrate",
        "code-review" => "Review",
        "ci" | "qa" => "Verify",
        "refactor" => "Improve",
        "ship" | "yeet" => "Ship",
        "flywheel" | "monitor" => "Operate",
        "reflect" => "Learn",
        "harness-engineering" => "Maintain",
        "research" => "Explore",
        "diagnose" => "Repair",
        _ => "Support",
    }
}

fn collect_gates(repo: &Path) -> Result<Vec<String>> {
    let text = fs::read_to_string(repo.join("ci/src/harness_kit_ci/main.py"))?;
    let regex = Regex::new(r#"run_gate,\s+"([^"]+)""#)?;
    let mut gates = regex
        .captures_iter(&text)
        .filter_map(|captures| captures.get(1).map(|value| value.as_str().to_string()))
        .collect::<BTreeSet<_>>()
        .into_iter()
        .collect::<Vec<_>>();
    gates.sort();
    Ok(gates)
}

fn collect_backlog(repo: &Path) -> Result<Vec<Value>> {
    let mut rows = Vec::new();
    let backlog = repo.join("backlog.d");
    if !backlog.exists() {
        return Ok(rows);
    }
    for entry in fs::read_dir(backlog)? {
        let path = entry?.path();
        if path.extension().and_then(|value| value.to_str()) != Some("md") {
            continue;
        }
        let Some(name) = path.file_name().and_then(|name| name.to_str()) else {
            continue;
        };
        if !name
            .chars()
            .next()
            .is_some_and(|character| character.is_ascii_digit())
        {
            continue;
        }
        let text = fs::read_to_string(&path)?;
        let title = text
            .lines()
            .next()
            .unwrap_or(name)
            .trim_start_matches('#')
            .trim();
        rows.push(json!({
            "title": title,
            "source": path.strip_prefix(repo)?.to_string_lossy().replace('\\', "/"),
        }));
    }
    rows.sort_by(|left, right| {
        left["source"]
            .as_str()
            .unwrap_or("")
            .cmp(right["source"].as_str().unwrap_or(""))
    });
    Ok(rows)
}

