fn copy_repo_for_self_test(source: &Path, destination: &Path) -> Result<()> {
    copy_dir_filtered(source, destination, &|path| {
        path.file_name()
            .and_then(|name| name.to_str())
            .is_some_and(|name| {
                matches!(
                    name,
                    ".agents"
                        | ".antigravitycli"
                        | ".claude"
                        | ".codex"
                        | ".git"
                        | ".pi"
                        | "__pycache__"
                        | ".venv"
                        | "target"
                )
            })
            || path.ends_with("skills/.external")
    })
}

fn copy_dir(source: &Path, destination: &Path) -> Result<()> {
    copy_dir_filtered(source, destination, &|_| false)
}

fn copy_dir_filtered(
    source: &Path,
    destination: &Path,
    skip: &impl Fn(&Path) -> bool,
) -> Result<()> {
    if skip(source) {
        return Ok(());
    }
    fs::create_dir_all(destination)?;
    for entry in fs::read_dir(source)? {
        let source_path = entry?.path();
        if skip(&source_path) {
            continue;
        }
        let destination_path = destination.join(
            source_path
                .file_name()
                .context("source path missing file name")?,
        );
        if source_path.is_dir() {
            copy_dir_filtered(&source_path, &destination_path, skip)?;
        } else {
            fs::copy(&source_path, destination_path)?;
        }
    }
    Ok(())
}

fn run_checked(command: &mut Command, label: &str) -> Result<()> {
    let output = command
        .output()
        .with_context(|| format!("failed to run {label}"))?;
    if !output.status.success() {
        bail!(
            "{label} failed: {}{}",
            String::from_utf8_lossy(&output.stdout),
            String::from_utf8_lossy(&output.stderr)
        );
    }
    Ok(())
}

