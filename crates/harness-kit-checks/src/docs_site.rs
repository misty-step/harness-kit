        let actual = temp.path().join("actual");
        fs::create_dir_all(&expected).unwrap();
        fs::create_dir_all(&actual).unwrap();
        fs::write(expected.join("missing.txt"), "left").unwrap();
        fs::write(expected.join("stale.txt"), "left").unwrap();
