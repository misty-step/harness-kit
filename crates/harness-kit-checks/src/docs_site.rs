fn compare_dirs(expected: &Path, actual: &Path) -> Result<Vec<String>> {
    let mut issues = Vec::new();
    compare_dirs_inner(expected, actual, &mut issues)?;
    Ok(issues)
}

fn compare_dirs_inner(expected: &Path, actual: &Path, issues: &mut Vec<String>) -> Result<()> {
    let expected_names = child_names(expected)?;
    let actual_names = child_names(actual)?;
    for name in expected_names.difference(&actual_names) {
        issues.push(format!(
            "missing generated path: {}",
            actual.join(name).display()
        ));
    }
    for name in actual_names.difference(&expected_names) {
        issues.push(format!(
            "unexpected generated path: {}",
            actual.join(name).display()
        ));
    }
    for name in expected_names.intersection(&actual_names) {
        let left = expected.join(name);
        let right = actual.join(name);
        if left.is_dir() && right.is_dir() {
            compare_dirs_inner(&left, &right, issues)?;
        } else if left.is_file() && right.is_file() {
            if fs::read(&left)? != fs::read(&right)? {
                issues.push(format!("stale generated path: {}", right.display()));
            }
        } else {
            issues.push(format!("stale generated path: {}", right.display()));
        }
    }
    Ok(())
}

fn child_names(path: &Path) -> Result<BTreeSet<String>> {
    let mut names = BTreeSet::new();
    if !path.exists() {
        return Ok(names);
    }
    for entry in fs::read_dir(path)? {
        names.insert(entry?.file_name().to_string_lossy().to_string());
    }
    Ok(names)
}

fn page_metadata(path: &Path) -> Result<(Option<String>, Option<String>)> {
    let text = fs::read_to_string(path)?;
    let primitive = Regex::new(r#"<meta name="harness-kit-primitive" content="([^"]+)">"#)?
        .captures(&text)
        .and_then(|captures| captures.get(1).map(|value| value.as_str().to_string()));
    let source = Regex::new(r#"<meta name="harness-kit-source" content="([^"]+)">"#)?
        .captures(&text)
        .and_then(|captures| captures.get(1).map(|value| value.as_str().to_string()));
    Ok((primitive, source))
}

fn html_img_srcs(text: &str) -> Vec<(String, String)> {
    let comments = Regex::new(r"(?s)<!--.*?-->").expect("valid regex");
    let text = comments.replace_all(text, "");
    let tag_regex = Regex::new(r"(?i)<img\b[^>]*>").expect("valid regex");
    let src_regex = Regex::new(r#"(?i)\bsrc\s*=\s*["']([^"']+)["']"#).expect("valid regex");
    let alt_regex = Regex::new(r#"(?i)\balt\s*=\s*["']([^"']*)["']"#).expect("valid regex");
    tag_regex
        .find_iter(&text)
        .filter_map(|tag| {
            let tag = tag.as_str();
            let src = src_regex
                .captures(tag)
                .and_then(|captures| captures.get(1).map(|value| value.as_str().to_string()))?;
            let alt = alt_regex
                .captures(tag)
                .and_then(|captures| captures.get(1).map(|value| value.as_str().to_string()))
                .unwrap_or_default();
            Some((src, alt))
        })
        .collect()
}

fn path_between_pages(page: &str, target: &str) -> String {
    let page_dir = Path::new(page).parent().unwrap_or_else(|| Path::new(""));
    if page_dir.as_os_str().is_empty() {
        return target.to_string();
    }
    let depth = page_dir.components().count();
    std::iter::repeat_n("..", depth)
        .chain(std::iter::once(target))
        .collect::<Vec<_>>()
        .join("/")
}

fn slug(value: &str) -> String {
    Regex::new(r"[^a-z0-9-]+")
        .expect("valid regex")
        .replace_all(&value.to_ascii_lowercase(), "-")
        .trim_matches('-')
        .to_string()
}

