use std::fs;
use std::path::{Path, PathBuf};
use std::process::Command;

use anyhow::{Context, Result};
use pulldown_cmark::{Options as MarkdownOptions, Parser, html};

pub struct RenderOptions {
    pub input: PathBuf,
    pub output: Option<PathBuf>,
    pub open: bool,
}

pub struct RenderReport {
    pub output: PathBuf,
    pub opened: bool,
}

pub fn render(options: &RenderOptions) -> Result<RenderReport> {
    let input = absolute_path(&options.input)?;
    let markdown = fs::read_to_string(&input)
        .with_context(|| format!("failed to read {}", input.display()))?;
    let title = title_from_markdown(&markdown).unwrap_or_else(|| {
        input
            .file_stem()
            .unwrap_or_default()
            .to_string_lossy()
            .into_owned()
    });
    let output = match &options.output {
        Some(output) => absolute_path(output)?,
        None => input.with_extension("html"),
    };
    if let Some(parent) = output.parent() {
        fs::create_dir_all(parent)
            .with_context(|| format!("failed to create {}", parent.display()))?;
    }
    fs::write(&output, render_html(&markdown, &title))
        .with_context(|| format!("failed to write {}", output.display()))?;
    if options.open {
        open_path(&output)?;
    }
    Ok(RenderReport {
        output,
        opened: options.open,
    })
}

fn absolute_path(path: &Path) -> Result<PathBuf> {
    if path.is_absolute() {
        Ok(path.to_path_buf())
    } else {
        Ok(std::env::current_dir()?.join(path))
    }
}

fn open_path(path: &Path) -> Result<()> {
    #[cfg(target_os = "macos")]
    let mut command = {
        let mut command = Command::new("open");
        command.arg(path);
        command
    };

    #[cfg(all(unix, not(target_os = "macos")))]
    let mut command = {
        let mut command = Command::new("xdg-open");
        command.arg(path);
        command
    };

    #[cfg(windows)]
    let mut command = {
        let mut command = Command::new("cmd");
        command.args(["/C", "start", ""]);
        command.arg(path);
        command
    };

    let status = command
        .status()
        .with_context(|| format!("failed to open {}", path.display()))?;
    anyhow::ensure!(
        status.success(),
        "open command failed for {}",
        path.display()
    );
    Ok(())
}

fn title_from_markdown(markdown: &str) -> Option<String> {
    markdown.lines().find_map(|line| {
        let title = line.strip_prefix("# ")?;
        let title = title.trim();
        (!title.is_empty()).then(|| title.to_string())
    })
}

