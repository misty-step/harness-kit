fn render_markdown(markdown: &str) -> String {
    let mut options = MarkdownOptions::empty();
    options.insert(MarkdownOptions::ENABLE_TABLES);
    options.insert(MarkdownOptions::ENABLE_FOOTNOTES);
    options.insert(MarkdownOptions::ENABLE_STRIKETHROUGH);
    options.insert(MarkdownOptions::ENABLE_TASKLISTS);
    let parser = Parser::new_ext(markdown, options);
    let mut rendered = String::new();
    html::push_html(&mut rendered, parser);
    rendered
}

fn escape_html(value: &str) -> String {
    value
        .replace('&', "&amp;")
        .replace('<', "&lt;")
        .replace('>', "&gt;")
        .replace('"', "&quot;")
        .replace('\'', "&#39;")
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn renders_review_html_with_headings_lists_and_code() {
        let html = render_html(
            "# Context Packet: Demo\n\n## Goal\nShip `x`.\n\n- Read files\n- Open review\n\n```sh\ncargo test\n```",
            "Context Packet: Demo",
        );
        assert!(html.contains("Harness Kit plan review"));
        assert!(html.contains("<h2>Goal</h2>"));
        assert!(html.contains("<code>x</code>"));
        assert!(html.contains("<li>Open review</li>"));
        assert!(html.contains("cargo test"));
    }

    #[test]
    fn defaults_output_next_to_input() {
        let temp = tempfile::tempdir().unwrap();
        let packet = temp.path().join("packet.md");
        fs::write(&packet, "# Packet\n\n## Goal\nReview me.").unwrap();
        let report = render(&RenderOptions {
            input: packet.clone(),
            output: None,
            open: false,
        })
        .unwrap();
        assert_eq!(report.output, packet.with_extension("html"));
        assert!(
            fs::read_to_string(report.output)
                .unwrap()
                .contains("Review me.")
        );
    }
}
